function product1() {
  let divEach = document
    .getElementById("pr1")
    .getElementsByTagName("img")[0].src;
  let nameEach = document
    .getElementById("pr1")
    .getElementsByTagName("h4")[0].innerText;
  let priceEach = document
    .getElementById("pr1")
    .getElementsByTagName("em")[0].innerText;
  console.log(priceEach);
  localStorage.setItem("prName", nameEach);
  localStorage.setItem("imgcustom", divEach);
  localStorage.setItem("priceEach", priceEach);
  window.location.href = "cart.html";
}

